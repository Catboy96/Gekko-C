#ifndef __GEKKO_H
#define __GEKKO_H
/**********************************************************************************************************************
    common definitions
**********************************************************************************************************************/
#ifndef PATH_MAX
#define PATH_MAX                        (512)
#endif
#ifndef NAME_MAX
#define NAME_MAX                        (255)
#endif
#ifndef TOKEN_MAX
#define TOKEN_MAX                       (32)
#endif
/**********************************************************************************************************************
    gekko defaults
**********************************************************************************************************************/
#ifdef WINDOWS
#define SEP                             "\\"
#else
#define SEP                             "/"
#endif

#define GEKKO_DEFAULT_CONFIG            SEP ".gekko" SEP "gekko.json"
/**********************************************************************************************************************
    gekko return type
**********************************************************************************************************************/
typedef enum {
    GEKKO_OK        = 0,
    GEKKO_ERROR     = 1,
} GEKKO_RETURN;
/**********************************************************************************************************************
    ssh authentication methods
**********************************************************************************************************************/
typedef enum {
    AUTH_METHOD_PASSWORD               = (1 << 0),
    AUTH_METHOD_KEYBOARD_INTERACTIVE   = (1 << 1),
    AUTH_METHOD_PUBLIC_KEY             = (1 << 2),
} AUTH_METHOD;
/**********************************************************************************************************************
    gekko grip type
**********************************************************************************************************************/
typedef struct {
    char            host[NAME_MAX];
    uint16_t        port;
    AUTH_METHOD     auth;
    char            user[NAME_MAX];
    char            pass[NAME_MAX];
    char            key[PATH_MAX];
} GRIP;

#endif  // __GEKKO_H
